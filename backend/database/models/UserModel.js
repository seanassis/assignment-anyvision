const { Schema } = require("mongoose");
const mongoose = require("mongoose");
const User = new Schema({
  first_name: {
    type: String,
    required: true
  },
  last_name: {
    type: String,
    required: true
  },
  username: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  phone_number: {
    type: String
  }
});
const user = mongoose.model("User", User);

module.exports = user;
